/*
*   PCAP01 发送8位操作指令
*/
static void pcap01_send_cmd(uint8_t cmd)
{
    user_iic_start();
    user_iic_send_byte(PCAP01_WRITE);
    user_iic_wait_ack();
    user_iic_send_byte(cmd);
    user_iic_wait_ack();
    user_iic_stop();
}


/*
*   PCAP01 向Sram中写入1字节数据
*/
static void pcap01_write_sram(uint16_t addr, uint8_t data)
{
    uint8_t value[4] = {0};

    uint16_t cmd = PCAP01_WRITE_SRAM | addr;

    value[0] = PCAP01_WRITE;
    value[1] = (cmd >> 8) & 0xFF;
    value[2] = cmd & 0xFF;
    value[3] = data;

    user_iic_start();

    for(uint8_t i = 0; i < sizeof(value); i++)
    {
        user_iic_send_byte(value[i]);
        user_iic_wait_ack();
    }

    user_iic_stop();
}


/*
*   PCAP01 从Sram中读取1字节数据
*/
static uint8_t pcap01_read_sram(uint16_t addr)
{
    uint8_t value[3] = {0};

    uint16_t cmd = PCAP01_READ_SRAM | addr;

    value[0] = PCAP01_WRITE;
    value[1] = (cmd >> 8) & 0xFF;
    value[2] = cmd & 0xFF;

    user_iic_start();
    for(uint8_t i = 0; i < sizeof(value); i++)
    {
        user_iic_send_byte(value[i]);
        user_iic_wait_ack();
    }

    user_iic_start();
    user_iic_send_byte(PCAP01_READ);
    user_iic_wait_ack();
    uint8_t data = user_iic_read_byte(false);
    user_iic_stop();

    return data;
}


/*
*   PCAP01 写入firmware
*/
static void pcap01_write_firmware(void)
{
    uint16_t i = 0;

    for(i = 0; i < sizeof(Firmware_data); i++)
    {
        pcap01_write_sram(i, Firmware_data[i]);
    }

    for(i = 0; i < sizeof(Firmware_data); i++)
    {
        if(Firmware_data[i] != pcap01_read_sram(i))
        {
            printf("firmware write failure, index is %d\r\n", i);
        }
    }

    printf("firmware write finished.\r\n");
}


/*
*   PCAP01 连接状态检测
*/
static bool pcap01_state_check(void)
{
    uint8_t test_data  = 0x5A;
    uint8_t test_count = 0;

    while(test_count < 20)
    {
        pcap01_write_sram(0x47, test_data);

        /* iic 读*/
        uint8_t data = pcap01_read_sram(0x47);

        if(data == test_data)
        {
            return true;
        }
        else
        {
            delay_us(1000);
            test_count++;
        }
    }

    return false;
}


/*
*   PCAP01 写寄存器
*/
static void pcap01_write_register(uint8_t addr, uint32_t param)
{
    uint8_t value[5] = {0};

    value[0] = PCAP01_WRITE;
    value[1] = PCAP01_WRITE_CFG | addr;
    value[2] = (param >> 16) & 0xFF;
    value[3] = (param >> 8) & 0xFF;
    value[4] = (param & 0xFF);

    user_iic_start();

    for(uint8_t i = 0; i < sizeof(value); i++)
    {
        user_iic_send_byte(value[i]);
        user_iic_wait_ack();
    }

    user_iic_stop();
}


/*
*   pcap01 配置寄存器((0-10)&20)参数，寄存器2为模式选择
*/
static void pcap01_cfg_reg(void)
{
	pcap01_write_register(0,  0x4200FF);    //slave mode：0100 0010 0000 0000 1111 1111:关闭SRAN与OTP的比较机制
    //关闭了(“single”)，limit=4032byte；从机模式，且固件能被更改；
	pcap01_write_register(1,  0x201022);    //50hz：0010 0000 0001 0000 0010 0010
    //这是被固定好的频率，无法更改；
	pcap01_write_register(2,  0x0F170B);  //*******1111 1111 0100 0110 0000 1011 //0x0F460B//0x0F450B//0x0F050B
    //使能了CDC端口，接地单一电容测量模式，内部放电电阻选择10KΩ
	//pcap01_write_register(3,  0x0D0001);    //3HZ：1101 0000 0000 0000 0001
    pcap01_write_register(3, 0x0D0004);    //3HZ：0001 0000 0000 0000 0001
    //CDC时钟循环基准为20us，SEQ_TIME=13,触发周期为0.16s；信噪比为1；
	//pcap01_write_register(4,  0x07E800);  // 0111 1110 1000 0000 0000//h xx xx 01  @abner 2019.10.30 setting based on datasheet
   pcap01_write_register(4,  0x043200);//CYCSLE=3ms；
    //pcap01_write_register(4,  0x080000);//0000 1010 0000 0000 0000 0000
    //PG0作为输出电容测量的脉冲触发管脚，持续模式，设置测量电容的周期时间为0.66ms，140us为周期用PG0做CMEAS触发来测量温度
	pcap01_write_register(5,  0x000000);    //h cx xx xx：RDC测量一次
	pcap01_write_register(6,  0x000040);    //h 00 43 40：进行两次假测量
	pcap01_write_register(7,  0x1F0000);    //0001 1111 0000 0000 0000 0000
	pcap01_write_register(8,  0x800030);    //1000 0000 0000 0000 0011 0000
    //选择程序存在SARM空间，PG0作为锁存触发的引脚，低功耗模式
	pcap01_write_register(9,  0xFF000F);
	pcap01_write_register(10, 0x180087);//设置为低功耗
	pcap01_write_register(20, 0x000001);//开启运行模式
    printf("register success!!!\n");
}


/*
*   PCAP01 读取结果寄存器
*/
float pcap01_read_res(uint8_t addr)
{
    uint8_t send_buf[2] = {0};//储存w801发送给pcap01ad的数据的buffer
    uint8_t read_buf[3] = {0};//储存w801从pcap01ad读取到的数据的buffer

    send_buf[0] = PCAP01_WRITE;//写命令
    send_buf[1] = PCAP01_READ_RES | addr;//读取地址为addr的的测量结果

    user_iic_start();
    for(uint8_t i = 0; i < sizeof(send_buf); i++)//一个字节发送一次
    {
        user_iic_send_byte(send_buf[i]);
        user_iic_wait_ack();
    }

    user_iic_start();
    user_iic_send_byte(PCAP01_READ);
    user_iic_wait_ack();
    read_buf[0] = user_iic_read_byte(true);
    read_buf[1] = user_iic_read_byte(true);
    read_buf[2] = user_iic_read_byte(false);
    user_iic_stop();

    return ((uint32_t)read_buf[0] << 16) | ((uint32_t)read_buf[1] << 8) | (uint32_t)read_buf[2];
}
/*
*   PCAP01 初始化
*/
void pcap01_init(void)
{
    uint8_t i = 0;

    user_iic_pin_cfg(pcap01_io[0], pcap01_io[1]);
    delay_us(10*1000);
    pcap01_send_cmd(PCAP01_POWER_RESET);
    delay_us(10*1000);
    user_iic_pin_cfg(pcap01_io[0], pcap01_io[1]);
    if(pcap01_state_check())
   {
     printf("[Pcap01]: index:%d, ready, ", i);
     pcap01_write_firmware();
     pcap01_cfg_reg();
     pcap01_send_cmd(PCAP01_SOFT_RESET);
     delay_us(1000);
     pcap01_send_cmd(PCAP01_MEAS_CAP);
     printf("cs start!!!\n");       
   }
    else
   {
     printf("[Pcap01]: index:%d, error!!!", i);
   }
    delay_us(500*1000);
}



